def _read_csv_with_common_encodings(input_path: Path) -> pd.DataFrame:
    """Read a CSV file, trying common Chinese CSV encodings."""
    if not input_path.exists():
        raise FileNotFoundError(f"未找到 CSV 文件：{input_path}")

    last_error: Exception | None = None
    for encoding in ("utf-8-sig", "utf-8", "gb18030"):
        try:
            return pd.read_csv(input_path, encoding=encoding)
        except UnicodeDecodeError as exc:
            last_error = exc

    raise ValueError(f"无法读取 CSV，请检查文件编码：{last_error}")


