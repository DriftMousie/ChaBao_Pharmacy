def run_sales_medical_compare(
    sales_input_path: Path,
    medical_input_path: Path,
    output_path: Path,
    column_config_path: Path,
) -> SalesMedicalCompareResult:
    """Compare sales-side orders with medical-side visit IDs and save the result."""
    print("正在读取销售端、医保端及列名配置文件...", flush=True)
    column_config = load_column_config(column_config_path)
    sales_df = load_compare_sales_data(sales_input_path, column_config)
    medical_df = load_compare_medical_data(medical_input_path, column_config)

    print("文件读取完成，正在汇总销售单和医保就诊记录...", flush=True)
    sales_bills = _summarize_bills(
        sales_df,
        bill_id_column=column_config.sales_order_id,
        amount_column=column_config.sales_amount,
        time_column=column_config.sales_bill_date,
    )
    medical_bills = _summarize_bills(
        medical_df,
        bill_id_column=column_config.medical_visit_id,
        amount_column=column_config.medical_amount,
        time_column=column_config.medical_settlement_time,
    )
    print(
        f"汇总完成：销售单 {len(sales_bills)} 个，医保就诊记录 {len(medical_bills)} 个。",
        flush=True,
    )

    sales_amount_index = _sales_bill_amount_index(sales_bills)
    sales_date_index, sales_hour_index = _sales_bill_time_indexes(sales_bills)
    used_sales_bill_ids: set[str] = set()
    bill_matches: dict[str, tuple[BillSummary, str]] = {}
    incomplete_matched_bill_count = 0
    third_matched_bill_count = 0

    print("开始第一轮完全匹配...", flush=True)
    for progress_index, medical_bill in enumerate(medical_bills, start=1):
        sales_bill = _find_best_sales_bill_match(
            medical_bill,
            sales_amount_index,
            used_sales_bill_ids,
            column_config,
        )
        if sales_bill is not None:
            used_sales_bill_ids.add(sales_bill.bill_id)
            bill_matches[medical_bill.bill_id] = (sales_bill, MATCH_STATUS_EXACT)
        _show_progress("第一轮比对进度", progress_index, len(medical_bills))

    unmatched_medical_bills = [
        medical_bill
        for medical_bill in medical_bills
        if medical_bill.bill_id not in bill_matches
    ]
    print(
        f"第一轮完成，完全匹配 {len(bill_matches)} 个；"
        f"开始第二轮不完全匹配，共 {len(unmatched_medical_bills)} 个待处理。",
        flush=True,
    )

    for progress_index, medical_bill in enumerate(unmatched_medical_bills, start=1):
        sales_bill = _find_best_secondary_sales_bill_match(
            medical_bill,
            sales_date_index,
            sales_hour_index,
            used_sales_bill_ids,
            column_config,
        )
        if sales_bill is not None:
            used_sales_bill_ids.add(sales_bill.bill_id)
            bill_matches[medical_bill.bill_id] = (sales_bill, MATCH_STATUS_INCOMPLETE)
            incomplete_matched_bill_count += 1
        _show_progress(
            "第二轮比对进度",
            progress_index,
            len(unmatched_medical_bills),
        )

    third_round_medical_bills = [
        medical_bill
        for medical_bill in unmatched_medical_bills
        if medical_bill.bill_id not in bill_matches
    ]
    print(
        f"第二轮完成，不完全匹配 {incomplete_matched_bill_count} 个；"
        f"开始第三轮金额与商品匹配，共 {len(third_round_medical_bills)} 个待处理。",
        flush=True,
    )
    for progress_index, medical_bill in enumerate(third_round_medical_bills, start=1):
        sales_bill = _find_best_third_sales_bill_match(
            medical_bill,
            sales_amount_index,
            used_sales_bill_ids,
            column_config,
        )
        if sales_bill is not None:
            used_sales_bill_ids.add(sales_bill.bill_id)
            bill_matches[medical_bill.bill_id] = (sales_bill, MATCH_STATUS_INCOMPLETE)
            incomplete_matched_bill_count += 1
            third_matched_bill_count += 1
        _show_progress(
            "第三轮比对进度",
            progress_index,
            len(third_round_medical_bills),
        )

    print("匹配完成，正在整理输出明细...", flush=True)
    output_rows: list[dict[str, object]] = []
    for progress_index, medical_bill in enumerate(medical_bills, start=1):
        matched_sales_bill, match_status = bill_matches.get(
            medical_bill.bill_id,
            (None, MATCH_STATUS_UNMATCHED),
        )
        output_rows.extend(
            _build_compare_rows(
                medical_bill,
                matched_sales_bill,
                match_status,
                column_config,
            )
        )
        _show_progress("输出明细整理进度", progress_index, len(medical_bills))

    result_df = pd.DataFrame(output_rows, columns=COMPARE_OUTPUT_COLUMNS)
    print("明细整理完成，正在写入结果文件...", flush=True)
    result_df.to_excel(output_path, index=False)
    print("结果文件写入完成。", flush=True)

    matched_bill_count = len(bill_matches)

    return SalesMedicalCompareResult(
        output_path=output_path,
        medical_bill_count=len(medical_bills),
        matched_bill_count=matched_bill_count,
        incomplete_matched_bill_count=incomplete_matched_bill_count,
        third_matched_bill_count=third_matched_bill_count,
        unmatched_bill_count=len(medical_bills) - matched_bill_count,
        output_rows=len(result_df),
    )


