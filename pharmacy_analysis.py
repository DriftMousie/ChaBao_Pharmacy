def _show_progress(label: str, current: int, total: int) -> None:
    """Print progress at roughly five-percent intervals without flooding the console."""
    if total <= 0:
        print(f"{label}：无待处理数据", flush=True)
        return

    interval = max(1, total // PROGRESS_UPDATE_STEPS)
    if current == 1 or current == total or current % interval == 0:
        percent = current * 100 / total
        print(f"{label}：{current}/{total}（{percent:.1f}%）", flush=True)


def _normalize_product_name(value: object) -> str:
    text = _text_value(value).lower()
    return re.sub(r"[\s（）()\[\]【】、，,。.;；:：\-_/\\·]+", "", text)


def _is_generic_medical_supply_name(value: object) -> bool:
    text = _text_value(value)
    return "械字号" in text or "消字号" in text


def _normalize_prescription_drug_name(value: object, remove_parenthetical: bool = False) -> str:
    text = _text_value(value).lower()
    if remove_parenthetical:
        previous = None
        while previous != text:
            previous = text
            text = re.sub(r"[（(\[【][^）)\]】]*[）)\]】]", "", text)
    return re.sub(r"[\s（）()\[\]【】、，,。.;；:：\-_/\\·]+", "", text)


def _prescription_drug_name_matches(
    medical_name: object,
    catalog_full_names: set[str],
    catalog_base_names: set[str],
) -> bool:
    medical_full_name = _normalize_prescription_drug_name(medical_name)
    medical_base_name = _normalize_prescription_drug_name(
        medical_name,
        remove_parenthetical=True,
    )
    if not medical_full_name:
        return False

    if medical_full_name in catalog_full_names or medical_base_name in catalog_base_names:
        return True

    for catalog_name in catalog_base_names:
        shorter_length = min(len(medical_base_name), len(catalog_name))
        longer_length = max(len(medical_base_name), len(catalog_name))
        if shorter_length < 4 or shorter_length / longer_length < 0.6:
            continue
        if medical_base_name in catalog_name or catalog_name in medical_base_name:
            return True

    return False


def _product_similarity(left: object, right: object) -> float:
    if _is_generic_medical_supply_name(left):
        return 1.0
    return _product_similarity_normalized(
        _normalize_product_name(left),
        _normalize_product_name(right),
    )


def _product_similarity_normalized(left_text: str, right_text: str) -> float:
    if not left_text or not right_text:
        return 0.0
    if left_text in right_text or right_text in left_text:
        shorter = min(len(left_text), len(right_text))
        longer = max(len(left_text), len(right_text))
        return 0.8 + 0.2 * (shorter / longer)
    return SequenceMatcher(None, left_text, right_text).ratio()


def _bill_product_similarity(
    medical_bill: BillSummary,
    sales_bill: BillSummary,
    column_config: ColumnConfig,
) -> float:
    medical_names = [
        _text_value(value)
        for value in medical_bill.items[column_config.medical_product_name]
    ]
    sales_names = [
        _text_value(value)
        for value in sales_bill.items[column_config.sales_product_name]
    ]
    if not medical_names or not sales_names:
        return 0.0

    scores = []
    for medical_name in medical_names:
        scores.append(max(_product_similarity(medical_name, sales_name) for sales_name in sales_names))
    return sum(scores) / len(scores)


def _amount_index_candidates(
    amount: float,
    sales_amount_index: dict[str, list[BillSummary]],
    tolerance: float,
) -> list[BillSummary]:
    if pd.isna(amount):
        return []
    center_cents = round(float(amount) * 100)
    tolerance_cents = max(0, int(round(tolerance * 100)))
    candidates = []
    seen_bill_ids: set[str] = set()
    for cents in range(center_cents - tolerance_cents, center_cents + tolerance_cents + 1):
        for sales_bill in sales_amount_index.get(f"{cents / 100:.2f}", []):
            if sales_bill.bill_id not in seen_bill_ids:
                candidates.append(sales_bill)
                seen_bill_ids.add(sales_bill.bill_id)
    return candidates


def _summarize_bills(
    df: pd.DataFrame,
    bill_id_column: str,
    amount_column: str,
    time_column: str,
) -> list[BillSummary]:
    working_df = df.copy()
    working_df["_analysis_bill_id"] = working_df[bill_id_column].map(_text_value)
    working_df["_analysis_amount"] = _numeric_series(working_df[amount_column])
    working_df["_analysis_time"] = _date_series(working_df[time_column])
    working_df = working_df[working_df["_analysis_bill_id"] != ""]

    summaries: list[BillSummary] = []
    for bill_id, group in working_df.groupby("_analysis_bill_id", sort=False):
        total_amount = group["_analysis_amount"].sum()
        valid_times = group["_analysis_time"].dropna()
        bill_time = valid_times.iloc[0] if not valid_times.empty else pd.NaT
        has_time_component = any(_value_has_time_component(value) for value in group[time_column])
        summaries.append(
            BillSummary(
                bill_id=bill_id, # type: ignore
                total_amount=float(total_amount),
                bill_time=bill_time, # type: ignore
                has_time_component=has_time_component,
                items=group.drop(
                    columns=["_analysis_bill_id", "_analysis_amount", "_analysis_time"]
                ),
                source_order=int(group.index.min()),
            )
        )
    return summaries


def _sales_bill_amount_index(sales_bills: list[BillSummary]) -> dict[str, list[BillSummary]]:
    index: dict[str, list[BillSummary]] = {}
    for sales_bill in sales_bills:
        index.setdefault(_amount_key(sales_bill.total_amount), []).append(sales_bill)
    return index


def _sales_bill_time_indexes(
    sales_bills: list[BillSummary],
) -> tuple[
    dict[pd.Timestamp, list[BillSummary]],
    dict[pd.Timestamp, list[BillSummary]],
]:
    """Index date-only bills by date and timed bills by hour for secondary matching."""
    date_index: dict[pd.Timestamp, list[BillSummary]] = {}
    hour_index: dict[pd.Timestamp, list[BillSummary]] = {}
    for sales_bill in sales_bills:
        if pd.isna(sales_bill.bill_time):
            continue
        bill_time = pd.Timestamp(sales_bill.bill_time)
        if sales_bill.has_time_component:
            hour_index.setdefault(bill_time.floor("h"), []).append(sales_bill)
        else:
            date_index.setdefault(bill_time.normalize(), []).append(sales_bill)
    return date_index, hour_index


def _secondary_sales_candidates(
    medical_bill: BillSummary,
    sales_date_index: dict[pd.Timestamp, list[BillSummary]],
    sales_hour_index: dict[pd.Timestamp, list[BillSummary]],
) -> list[BillSummary]:
    medical_time = pd.Timestamp(medical_bill.bill_time)
    candidates = list(sales_date_index.get(medical_time.normalize(), []))
    medical_hour = medical_time.floor("h")
    tolerance = int(SECONDARY_COMPARE_TIME_TOLERANCE_HOURS)
    for hour_offset in range(-tolerance, tolerance + 1):
        candidate_hour = medical_hour + pd.Timedelta(hours=hour_offset)
        candidates.extend(sales_hour_index.get(candidate_hour, []))
    return candidates


def _find_best_sales_bill_match(
    medical_bill: BillSummary,
    sales_amount_index: dict[str, list[BillSummary]],
    used_sales_bill_ids: set[str],
    column_config: ColumnConfig,
) -> BillSummary | None:
    candidates = []
    for sales_bill in _amount_index_candidates(
        medical_bill.total_amount,
        sales_amount_index,
        AMOUNT_TOLERANCE,
    ):
        if sales_bill.bill_id in used_sales_bill_ids:
            continue
        if not _same_amount(medical_bill.total_amount, sales_bill.total_amount):
            continue

        is_time_match, time_distance = _sales_time_match_distance(
            medical_bill,
            sales_bill,
            COMPARE_TIME_TOLERANCE_HOURS,
        )
        if not is_time_match:
            continue

        candidates.append((sales_bill, time_distance))

    if not candidates:
        return None

    nearest_time_distance = min(time_distance for _, time_distance in candidates)
    nearest_candidates = [
        sales_bill
        for sales_bill, time_distance in candidates
        if time_distance == nearest_time_distance
    ]

    def sort_key(sales_bill: BillSummary) -> tuple[float, int]:
        return (
            -_bill_product_similarity(medical_bill, sales_bill, column_config),
            sales_bill.source_order,
        )

    return min(nearest_candidates, key=sort_key)


def _find_best_secondary_sales_bill_match(
    medical_bill: BillSummary,
    sales_date_index: dict[pd.Timestamp, list[BillSummary]],
    sales_hour_index: dict[pd.Timestamp, list[BillSummary]],
    used_sales_bill_ids: set[str],
    column_config: ColumnConfig,
) -> BillSummary | None:
    if pd.isna(medical_bill.bill_time):
        return None

    candidates = []
    for sales_bill in _secondary_sales_candidates(
        medical_bill,
        sales_date_index,
        sales_hour_index,
    ):
        if sales_bill.bill_id in used_sales_bill_ids:
            continue

        amount_distance = _safe_abs_diff(
            medical_bill.total_amount,
            sales_bill.total_amount,
        )
        if amount_distance > SECONDARY_COMPARE_AMOUNT_TOLERANCE:
            continue

        is_time_match, time_distance = _sales_time_match_distance(
            medical_bill,
            sales_bill,
            SECONDARY_COMPARE_TIME_TOLERANCE_HOURS,
        )
        if not is_time_match:
            continue

        product_similarity = _bill_product_similarity(
            medical_bill,
            sales_bill,
            column_config,
        )
        candidates.append(
            (sales_bill, amount_distance, time_distance, product_similarity)
        )

    if not candidates:
        return None

    def sort_key(
        candidate: tuple[BillSummary, float, float, float],
    ) -> tuple[float, float, float, int]:
        sales_bill, amount_distance, time_distance_hours, product_similarity = candidate
        return (
            amount_distance,
            time_distance_hours,
            -product_similarity,
            sales_bill.source_order,
        )

    return min(candidates, key=sort_key)[0]


def _find_best_third_sales_bill_match(
    medical_bill: BillSummary,
    sales_amount_index: dict[str, list[BillSummary]],
    used_sales_bill_ids: set[str],
    column_config: ColumnConfig,
) -> BillSummary | None:
    candidates = []
    for sales_bill in _amount_index_candidates(
        medical_bill.total_amount,
        sales_amount_index,
        THIRD_COMPARE_AMOUNT_TOLERANCE,
    ):
        if sales_bill.bill_id in used_sales_bill_ids:
            continue
        if not _same_amount(
            medical_bill.total_amount,
            sales_bill.total_amount,
            THIRD_COMPARE_AMOUNT_TOLERANCE,
        ):
            continue
        product_similarity = _bill_product_similarity(
            medical_bill,
            sales_bill,
            column_config,
        )
        if product_similarity < THIRD_COMPARE_PRODUCT_SIMILARITY_THRESHOLD:
            continue
        candidates.append((sales_bill, product_similarity))

    if not candidates:
        return None
    return min(
        candidates,
        key=lambda candidate: (-candidate[1], candidate[0].source_order),
    )[0]


