def _numeric_series(series: pd.Series) -> pd.Series:
    return pd.to_numeric(series, errors="coerce")


def _date_series(series: pd.Series) -> pd.Series:
    return pd.to_datetime(series, errors="coerce")


def _value_has_time_component(value: object) -> bool:
    if pd.isna(value): # type: ignore
        return False

    text = str(value).strip()
    if re.search(r"\d{1,2}:\d{2}", text):
        parsed = pd.to_datetime(value, errors="coerce") # type: ignore
        if pd.isna(parsed):
            return True
        timestamp = pd.Timestamp(parsed)
        return any(
            [
                timestamp.hour,
                timestamp.minute,
                timestamp.second,
                timestamp.microsecond,
                timestamp.nanosecond,
            ]
        )

    parsed = pd.to_datetime(value, errors="coerce") # type: ignore
    if pd.isna(parsed):
        return False

    timestamp = pd.Timestamp(parsed)
    return any(
        [
            timestamp.hour,
            timestamp.minute,
            timestamp.second,
            timestamp.microsecond,
            timestamp.nanosecond,
        ]
    )


def _same_quantity(quantity: pd.Series, target_quantity: float) -> pd.Series:
    return (quantity - target_quantity).abs() <= QUANTITY_TOLERANCE


def _safe_abs_diff(left: float, right: float) -> float:
    if pd.isna(left) or pd.isna(right):
        return float("inf")
    return abs(float(left) - float(right))


def _date_distance_days(left: pd.Timestamp, right: pd.Timestamp) -> float:
    if pd.isna(left) or pd.isna(right):
        return float("inf")
    left_date = pd.Timestamp(left).normalize()
    right_date = pd.Timestamp(right).normalize()
    return abs((left_date - right_date).days)


def _time_distance_hours(left: pd.Timestamp, right: pd.Timestamp) -> float:
    if pd.isna(left) or pd.isna(right):
        return float("inf")
    return abs((pd.Timestamp(left) - pd.Timestamp(right)).total_seconds() / 3600)


def _same_date(left: pd.Timestamp, right: pd.Timestamp) -> bool:
    if pd.isna(left) or pd.isna(right):
        return False
    return pd.Timestamp(left).normalize() == pd.Timestamp(right).normalize()


def _sales_time_match_distance(
    medical_bill: BillSummary,
    sales_bill: BillSummary,
    tolerance_hours: float,
) -> tuple[bool, float]:
    if not sales_bill.has_time_component:
        return _same_date(medical_bill.bill_time, sales_bill.bill_time), 0.0

    time_distance_hours = _time_distance_hours(medical_bill.bill_time, sales_bill.bill_time)
    return time_distance_hours <= tolerance_hours, time_distance_hours


