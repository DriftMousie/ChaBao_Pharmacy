def _align_sales_items_to_medical(
    medical_names: list[str],
    sales_items: list[dict[str, str]],
) -> list[dict[str, str] | None]:
    aligned: list[dict[str, str] | None] = []
    used_sales_indices: set[int] = set()

    for medical_name in medical_names:
        available_indices = [
            index for index in range(len(sales_items)) if index not in used_sales_indices
        ]
        if not available_indices:
            aligned.append(None)
            continue

        best_index = max(
            available_indices,
            key=lambda index: (
                _product_similarity(medical_name, sales_items[index]["name"]),
                -index,
            ),
        )
        used_sales_indices.add(best_index)
        aligned.append(sales_items[best_index])

    for index, sales_item in enumerate(sales_items):
        if index not in used_sales_indices:
            aligned.append(sales_item)

    return aligned


