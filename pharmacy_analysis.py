def validate_required_columns(df: pd.DataFrame, required_columns: Iterable[str]) -> None:
    """Raise a clear error if the input file is missing required columns."""
    missing_columns = [column for column in required_columns if column not in df.columns]
    if missing_columns:
        missing = "、".join(missing_columns)
        raise ValueError(f"输入表缺少必要列：{missing}")


def load_column_config(config_path: Path) -> ColumnConfig:
    """Load user-entered column names from the column config workbook."""
    if not config_path.exists():
        raise FileNotFoundError(f"未找到列名配置文件：{config_path}")

    try:
        df = pd.read_excel(config_path, sheet_name=COLUMN_CONFIG_SHEET)
    except ValueError:
        df = pd.read_excel(config_path)

    validate_required_columns(
        df,
        [COLUMN_CONFIG_KEY_COLUMN, COLUMN_CONFIG_DEFAULT_COLUMN, COLUMN_CONFIG_USER_COLUMN],
    )

    configured_columns = {}
    for _, row in df.iterrows():
        field_key = _text_value(row[COLUMN_CONFIG_KEY_COLUMN])
        if field_key not in COLUMN_CONFIG_FIELDS:
            continue

        user_column = _text_value(row[COLUMN_CONFIG_USER_COLUMN])
        default_column = _text_value(row[COLUMN_CONFIG_DEFAULT_COLUMN])
        configured_columns[field_key] = user_column or default_column

    return ColumnConfig(
        **{
            field_key: configured_columns.get(
                field_key,
                getattr(DEFAULT_COLUMN_CONFIG, field_key),
            )
            for field_key in COLUMN_CONFIG_FIELDS
        }
    )


