def _format_time_difference(left: pd.Timestamp, right: pd.Timestamp) -> str:
    if pd.isna(left) or pd.isna(right):
        return ""

    total_minutes = int(abs((pd.Timestamp(left) - pd.Timestamp(right)).total_seconds()) // 60)
    hours, minutes = divmod(total_minutes, 60)
    if hours and minutes:
        return f"{hours}小时{minutes}分钟"
    if hours:
        return f"{hours}小时"
    return f"{minutes}分钟"


def _find_best_prescription_match(
    medical_row: pd.Series,
    medical_time: pd.Timestamp,
    prescription_lookup: dict[str, pd.DataFrame],
    used_prescription_indices: set[object],
    column_config: ColumnConfig,
) -> object | None:
    medical_person_name = _text_value(medical_row[column_config.medical_person_name])
    medical_product_name = _normalize_product_name(medical_row[column_config.medical_product_name])

    if not medical_person_name or pd.isna(medical_time):
        return None

    person_prescriptions = prescription_lookup.get(medical_person_name)
    if person_prescriptions is None or person_prescriptions.empty:
        return None

    available_prescriptions = person_prescriptions[
        ~person_prescriptions.index.isin(used_prescription_indices)
    ]
    if available_prescriptions.empty:
        return None

    time_distances = (
        pd.Timestamp(medical_time) - available_prescriptions["_prescription_submit_time"]
    ).abs().dt.total_seconds() / 3600
    available_prescriptions = available_prescriptions[
        time_distances <= PRESCRIPTION_COMPARE_TIME_TOLERANCE_HOURS
    ]
    if available_prescriptions.empty:
        return None

    candidates = []
    for prescription_index, prescription_row in available_prescriptions.iterrows():
        product_similarity = _product_similarity_normalized(
            medical_product_name,
            prescription_row["_prescription_product_name"],
        )
        if product_similarity < PRESCRIPTION_PRODUCT_SIMILARITY_THRESHOLD:
            continue

        candidates.append(
            (
                prescription_index,
                -product_similarity,
                time_distances.at[prescription_index],
                prescription_row["_prescription_source_order"],
            )
        )

    if not candidates:
        return None

    return min(candidates, key=lambda candidate: candidate[1:])[0]


def _build_prescription_lookup(
    prescription_df: pd.DataFrame,
    prescription_times: pd.Series,
    column_config: ColumnConfig,
) -> dict[str, pd.DataFrame]:
    lookup_df = pd.DataFrame(
        {
            "_prescription_person_name": prescription_df[
                column_config.prescription_person_name
            ].map(_text_value),
            "_prescription_submit_time": prescription_times,
            "_prescription_product_name": prescription_df[
                column_config.prescription_product_name
            ].map(_normalize_product_name),
            "_prescription_source_order": range(len(prescription_df)),
        },
        index=prescription_df.index,
    )
    lookup_df = lookup_df[
        (lookup_df["_prescription_person_name"] != "")
        & lookup_df["_prescription_submit_time"].notna()
        & (lookup_df["_prescription_product_name"] != "")
    ]
    return {
        person_name: person_df
        for person_name, person_df in lookup_df.groupby("_prescription_person_name", sort=False)
    } # type: ignore


def compare_prescriptions_to_medical(
    medical_df: pd.DataFrame,
    prescription_df: pd.DataFrame,
    column_config: ColumnConfig,
) -> tuple[pd.DataFrame, int, int, int]:
    """
    Mark each medical-side row with its prescription status.

    Matching rule:
    1. Medical person name equals prescription person name.
    2. Settlement time and prescription submit time are within 1 hour.
    3. Medical product name and prescription product name are similar enough.
    """
    validate_required_columns(medical_df, column_config.prescription_medical_required_columns)
    validate_required_columns(prescription_df, column_config.prescription_required_columns)

    result_df = medical_df.copy()
    original_medical_columns = [
        column
        for column in result_df.columns
        if column not in PRESCRIPTION_MATCH_OUTPUT_COLUMNS
    ]
    for column in PRESCRIPTION_MATCH_OUTPUT_COLUMNS:
        result_df[column] = ""
    result_df["处方情况"] = PRESCRIPTION_STATUS_MISSING

    prescription_times = _date_series(prescription_df[column_config.prescription_submit_time])
    medical_times = _date_series(result_df[column_config.medical_settlement_time])
    prescription_lookup = _build_prescription_lookup(
        prescription_df,
        prescription_times,
        column_config,
    )
    used_prescription_indices: set[object] = set()
    normal_rows = 0
    after_medicine_rows = 0

    total_medical_rows = len(result_df)
    for progress_index, (medical_index, medical_row) in enumerate(
        result_df.iterrows(),
        start=1,
    ):
        prescription_index = _find_best_prescription_match(
            medical_row,
            medical_times.at[medical_index], # type: ignore
            prescription_lookup,
            used_prescription_indices,
            column_config,
        )
        if prescription_index is None:
            _show_progress("处方医保比对进度", progress_index, total_medical_rows)
            continue

        used_prescription_indices.add(prescription_index)
        medical_time = medical_times.at[medical_index]
        prescription_time = prescription_times.at[prescription_index]
        result_df.at[medical_index, "处方端门诊号"] = prescription_df.at[
            prescription_index,
            column_config.prescription_outpatient_id,
        ]
        result_df.at[medical_index, "处方端问诊人"] = prescription_df.at[
            prescription_index,
            column_config.prescription_person_name,
        ]
        result_df.at[medical_index, "处方端提交时间"] = prescription_df.at[
            prescription_index,
            column_config.prescription_submit_time,
        ]
        result_df.at[medical_index, "处方端药品名"] = prescription_df.at[
            prescription_index,
            column_config.prescription_product_name,
        ]

        if pd.Timestamp(medical_time) < pd.Timestamp(prescription_time): # type: ignore
            result_df.at[medical_index, "处方情况"] = PRESCRIPTION_STATUS_AFTER_MEDICINE
            result_df.at[medical_index, "相差时间"] = _format_time_difference(
                medical_time, # type: ignore
                prescription_time, # type: ignore
            )
            after_medicine_rows += 1
        else:
            result_df.at[medical_index, "处方情况"] = PRESCRIPTION_STATUS_NORMAL
            normal_rows += 1

        _show_progress("处方医保比对进度", progress_index, total_medical_rows)

    missing_rows = int((result_df["处方情况"] == PRESCRIPTION_STATUS_MISSING).sum())
    result_df = result_df[original_medical_columns + PRESCRIPTION_MATCH_OUTPUT_COLUMNS]
    return result_df, normal_rows, after_medicine_rows, missing_rows


def compare_medical_sales_to_prescription_drugs(
    medical_df: pd.DataFrame,
    catalog_df: pd.DataFrame,
    medical_product_column: str = COL_MEDICAL_PRODUCT_NAME,
) -> tuple[pd.DataFrame, int, int]:
    """
    Mark whether each medical-side product is listed in the prescription-drug catalog.

    Product names are normalized before matching. Parenthetical brand or manufacturer
    suffixes are ignored, while the main drug name and dosage form remain significant.
    """
    validate_required_columns(medical_df, [medical_product_column])
    validate_required_columns(catalog_df, [COL_PRESCRIPTION_DRUG_CATALOG_PRODUCT_NAME])

    catalog_names = catalog_df[COL_PRESCRIPTION_DRUG_CATALOG_PRODUCT_NAME].dropna()
    catalog_full_names = {
        normalized_name
        for value in catalog_names
        if (normalized_name := _normalize_prescription_drug_name(value))
    }
    catalog_base_names = {
        normalized_name
        for value in catalog_names
        if (
            normalized_name := _normalize_prescription_drug_name(
                value,
                remove_parenthetical=True,
            )
        )
    }

    result_df = medical_df.copy()
    match_results = []
    total_medical_rows = len(result_df)
    for progress_index, value in enumerate(
        result_df[medical_product_column],
        start=1,
    ):
        match_results.append(
            _prescription_drug_name_matches(
                value,
                catalog_full_names,
                catalog_base_names,
            )
        )
        _show_progress("处方药目录比对进度", progress_index, total_medical_rows)
    matched_mask = pd.Series(match_results, index=result_df.index, dtype=bool)
    result_df[COL_IS_PRESCRIPTION_DRUG] = matched_mask.map(
        {
            True: PRESCRIPTION_DRUG_MATCHED,
            False: PRESCRIPTION_DRUG_UNMATCHED,
        }
    )

    prescription_drug_rows = int(matched_mask.sum())
    non_prescription_drug_rows = len(result_df) - prescription_drug_rows
    return result_df, prescription_drug_rows, non_prescription_drug_rows


def _approval_mismatch(left: object, right: object) -> int:
    if pd.isna(left) or pd.isna(right):# type: ignore
        return 1
    return 0 if str(left).strip() == str(right).strip() else 1


