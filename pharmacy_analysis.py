def _find_best_positive_match(
    df: pd.DataFrame,
    quantities: pd.Series,
    dates: pd.Series,
    prices: pd.Series,
    amounts: pd.Series,
    negative_index: int,
    used_positive_indices: set[int],
    column_config: ColumnConfig,
) -> int | None:
    negative_quantity = quantities.at[negative_index]
    if pd.isna(negative_quantity):
        return None

    target_quantity = abs(float(negative_quantity))# type: ignore
    product_name = df.at[negative_index, column_config.sales_product_name]

    candidates = df.index[
        (df[column_config.sales_product_name] == product_name)
        & (quantities > 0)
        & _same_quantity(quantities, target_quantity)
    ]
    candidates = [index for index in candidates if index not in used_positive_indices]
    if not candidates:
        return None

    negative_date = dates.at[negative_index]
    negative_price = prices.at[negative_index]
    negative_amount_abs = abs(amounts.at[negative_index]) if not pd.isna(amounts.at[negative_index]) else float("nan")# type: ignore
    negative_approval_no = df.at[negative_index, column_config.sales_approval_no]

    def sort_key(candidate_index: int) -> tuple[float, int, float, float, int]:
        return (
            _date_distance_days(negative_date, dates.at[candidate_index]), # type: ignore
            _approval_mismatch(
                negative_approval_no,
                df.at[candidate_index, column_config.sales_approval_no],
            ),
            _safe_abs_diff(negative_price, prices.at[candidate_index]),# type: ignore
            _safe_abs_diff(negative_amount_abs, amounts.at[candidate_index]),# type: ignore
            int(candidate_index),
        )

    return min(candidates, key=sort_key)


def mark_return_offsets(
    df: pd.DataFrame,
    column_config: ColumnConfig,
) -> tuple[pd.DataFrame, int, int, int]:
    """
    Add/overwrite the configured offset column and mark return offset pairs.

    Matching rule:
    1. Start from rows whose quantity is negative.
    2. Find an unused positive row with the same product name and the opposite
       quantity.
    3. If multiple rows qualify, choose the row with the nearest bill date.
       Approval number, price, amount, and original row order are tie-breakers.
    """
    validate_required_columns(df, column_config.return_required_columns)

    result_df = df.copy()
    result_df[column_config.sales_offset_id] = ""

    quantities = _numeric_series(result_df[column_config.sales_quantity])
    dates = _date_series(result_df[column_config.sales_bill_date])
    prices = _numeric_series(result_df[column_config.sales_price])
    amounts = _numeric_series(result_df[column_config.sales_amount])

    negative_indices = list(result_df.index[quantities < 0])
    used_positive_indices: set[int] = set()
    matched_pairs = 0

    total_negative_rows = len(negative_indices)
    for progress_index, negative_index in enumerate(negative_indices, start=1):
        positive_index = _find_best_positive_match(
            result_df,
            quantities,
            dates,
            prices,
            amounts,
            negative_index,
            used_positive_indices,
            column_config,
        )
        if positive_index is None:
            _show_progress("退货冲销比对进度", progress_index, total_negative_rows)
            continue

        matched_pairs += 1
        offset_id = f"{matched_pairs:03d}"
        result_df.at[negative_index, column_config.sales_offset_id] = offset_id
        result_df.at[positive_index, column_config.sales_offset_id] = offset_id
        used_positive_indices.add(positive_index)
        _show_progress("退货冲销比对进度", progress_index, total_negative_rows)

    unmatched_negative_rows = len(negative_indices) - matched_pairs
    return result_df, len(negative_indices), matched_pairs, unmatched_negative_rows


